    new_state_dict = state_dict
    if is_comp: new_state_dict = OrderedDict((k.replace('_orig_mod.', '', 1), v) for k, v in new_state_dict.items()); first_key = list(new_state_dict.keys())[0]; is_dp = first_key.startswith('module.')
